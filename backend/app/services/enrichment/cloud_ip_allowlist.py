def is_known_cloud_ip(ip: str) -> bool:
    """Check if an IP belongs to a known cloud provider."""
    if not ip:
        return False
    return any(ip.startswith(prefix) for prefix in _CLOUD_IP_PREFIXES)


def is_known_cloud_org(org: str) -> bool:
    """Check if an organization name matches a known cloud provider."""
    if not org:
        return False
    org_lower = org.lower()
    return any(provider in org_lower for provider in _CLOUD_PROVIDER_ORGS)


def check_cloud_provider(raw_alert: dict[str, Any]) -> bool:
    """Check if any destination IP in the alert belongs to a known cloud provider.

    Sets `_knownCloudProvider = True` on the raw_alert if found.
    Used by enrichment to fire a negative signal that suppresses
    false-positive C2 scores on Microsoft/Google/AWS/GitHub traffic.
    """
    ips_list = raw_alert.get("ips") or []
    for ip_entry in ips_list:
        if isinstance(ip_entry, dict):
            ip_addr = ip_entry.get("ipAddress", "") or ""
        elif isinstance(ip_entry, str):
            ip_addr = ip_entry
        else:
            continue
        if ip_addr and is_known_cloud_ip(ip_addr):
            raw_alert["_knownCloudProvider"] = True
            raw_alert["_knownCloudProviderIP"] = ip_addr
            return True

    # Also check raw IP fields
    for field in ("dst_ip", "_dstIp", "destinationIp"):
        ip = raw_alert.get(field, "") or ""
        if ip and is_known_cloud_ip(ip):
            raw_alert["_knownCloudProvider"] = True
            raw_alert["_knownCloudProviderIP"] = ip
            return True

    return False
